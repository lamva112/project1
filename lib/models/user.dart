class User {
  final String ImagePath;
  final String FirstName;
  final String LastName;
  final String email;
  final String about;
  final String DayOfBith;
  final String Gender;

  const User({
    required this.ImagePath,
    required this.FirstName,
    required this.LastName,
    required this.email,
    required this.about,
    required this.DayOfBith,
    required this.Gender,
  });
}
